/**
 * 챕터별 집필 프롬프트 빌더
 * - 챕터 타입별 전문 가이드 적용
 * - 컨텍스트 자동 주입
 */
class PromptBuilder {
  // ── 챕터 타입별 집필 지침 ───────────────────────────
  static TYPE_GUIDE = {
    '프롤로그': `고백체 서술. 저자의 개인 경험에서 시작하여 독자가 "나도 그래"를 느끼게 하라.
첫 문장은 독자를 즉각 끌어당기는 현장 묘사나 대화로 시작.
마지막은 이 책이 왜 필요한지 독자가 느끼게 마무리.`,

    '르포': `르포르타주 스타일. 3인칭 근접 시점으로 현장을 재현.
취재 대상의 하루를 시간 순서로 따라가며 관찰과 묘사만. 저자 의견 삽입 금지.
인물의 표정, 몸짓, 공간의 냄새·소리까지 세밀하게 묘사.
마지막 문장: "— [인물]의 이야기는 여기서 멈춘다." 형식으로 마무리.`,

    '에세이': `지적 발견의 에세이. 데이터와 역사 사례를 결합한 논증.
"~해야 한다" "~해라" 형식 절대 금지. 독자 스스로 결론에 도달하게 유도.
구체적 통계·연구 결과로 논거를 강화. 마지막은 열린 질문으로 마무리.`,

    '대화': `직설적 대화 형식. 독자의 날카로운 질문에 저자가 솔직하게 답변.
"소주 한잔하며 나누는 진심" 톤. 격식 없이 구어체로.
각 대화 세트(질문+답변)는 한 가지 핵심 메시지를 전달.
마지막: 한 줄 결론 문장으로 마무리.`,

    '에필로그': `편지 형식. 미래의 독자에게 쓰는 따뜻한 편지.
"당신"이라고 직접 호칭. 담담하지만 따뜻한 온기.
이 책 전체의 여정을 돌아보며 독자에게 용기와 희망을 전달.`,

    '인터뷰': `실제 인터뷰를 재현하는 형식. Q&A 구조.
Q는 독자가 가장 궁금해할 핵심 질문. A는 인터뷰이의 목소리로.
중간중간 인터뷰 현장 묘사(표정, 분위기) 삽입.`,

    '칼럼': `전문가 칼럼 스타일. 짧고 명확한 문장.
하나의 핵심 주장을 도입→전개→결론 구조로.
독자 행동 변화를 이끌어내는 마무리.`,
  };

  // ── 최종 프롬프트 조립 ──────────────────────────────
  static build(chapter, bookBible, contextBlock, chapterIdx = 0) {
    const guide = PromptBuilder.TYPE_GUIDE[chapter.type] || '해당 챕터 특성에 맞게 자연스럽게 작성.';
    const charTarget = (chapter.targetChars || 30000).toLocaleString();
    const partName = ['프롤로그','PART 1','PART 2','PART 3','PART 4','에필로그·부록'][chapter.partNum] || `PART ${chapter.partNum}`;

    return `당신은 한국 베스트셀러 논픽션 작가입니다. 지금 『${bookBible?.title || '도서'}』를 집필 중입니다.

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
## 집필 대상 챕터
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
- 파트: ${partName}
- 챕터 번호: ${chapter.num}
- 타입: ${chapter.type}
- 제목: 『${chapter.title}』
- 목표 분량: ${charTarget}자 (한국어 기준, 반드시 달성)
- 챕터 요약: ${chapter.summary || '기획서 참고'}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
## 도서 일관성 컨텍스트
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
${contextBlock || '(첫 챕터 — 컨텍스트 없음)'}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
## 집필 지침
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
${guide}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
## 집필 규칙 (반드시 준수)
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
1. 목표 분량 ${charTarget}자를 반드시 채울 것 (이보다 짧으면 실패)
2. 제목이나 챕터 번호를 본문 첫 줄에 쓰지 말 것
3. 줄바꿈을 충분히 사용하여 가독성 확보
4. 이전 챕터와 자연스럽게 연결되는 도입부 작성
5. 한국어 논픽션 특유의 생생한 문체 유지
6. 지금 바로 본문을 시작하라 (서두, 설명, 인사말 없이)

지금 즉시 집필을 시작하라:`;
  }
}

module.exports = PromptBuilder;
